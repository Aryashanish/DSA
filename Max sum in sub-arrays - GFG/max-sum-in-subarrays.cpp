class Solution{
    public:
        long long pairWithMaxSum(long long arr[], long long n)
    {
        // Your code goes here
        int i = 0 , j = 1;
        long long ans = 0;
        while(j<n){
            ans = max(ans , arr[i]+arr[j]);
            i++;
            j++;
        }
        return ans;
    }
};



